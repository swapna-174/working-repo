            ON hsp.PRINCIPALPROBLEMKEY = t.DIAGNOSISKEY
        LEFT JOIN CABOODLE.DIAGNOSISDIM dx
            ON hsp.PRINCIPALPROBLEMKEY = dx.DIAGNOSISKEY
        WHERE t.TYPE = 'ICD-10-CM'
          AND hsp.PRINCIPALPROBLEMKEY IS NOT NULL
          AND hsp.ADMISSIONDATEKEY IS NOT NULL
          AND DATEDIFF(DAY, idx.DISCHARGE_DATE, hdd.DATEVALUE) BETWEEN 31 AND 90
        QUALIFY rn = 1
    ) s
    GROUP BY SurgicalCaseKey
),

CTE_ED_VISIT_30 AS (
    SELECT
        SurgicalCaseKey,
        MAX(CASE WHEN rn = 1 THEN ed_dt END) AS ED_VISIT_DATE_30D,
        MAX(CASE WHEN rn = 1 THEN ed_reason END) AS ED_VISIT_REASON_30D
    FROM (
        SELECT
            S.SURGICALCASEKEY AS SurgicalCaseKey,
            edd.DATEVALUE AS ed_dt,
            COALESCE(ccd.NAME, 'Not documented') AS ed_reason,
            ROW_NUMBER() OVER (
                PARTITION BY S.SURGICALCASEKEY
                ORDER BY edd.DATEVALUE ASC, ev.ARRIVALDATEKEY ASC
            ) AS rn
        FROM CTE_SURGERY_BASE S
        JOIN CABOODLE.EDVISITFACT ev
            ON ev.PATIENTDURABLEKEY = S.PDK
        JOIN CABOODLE.DATEDIM edd
            ON ev.ARRIVALDATEKEY = edd.DATEKEY
        LEFT JOIN CABOODLE.CHIEFCOMPLAINTDIM ccd
            ON ev.PRIMARYCHIEFCOMPLAINTKEY = ccd.CHIEFCOMPLAINTKEY
        WHERE DATEDIFF(DAY, S.SURGERY_DATE, edd.DATEVALUE) BETWEEN 0 AND 30
    ) x
    GROUP BY SurgicalCaseKey
)

