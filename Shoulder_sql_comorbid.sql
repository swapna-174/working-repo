            BA.FINANCIALCLASS,
            BA.ADMISSIONDATEKEY,
            BA.ADMISSIONTIMEOFDAYKEY,
            BA.DISCHARGEDATEKEY,
            BA.DISCHARGETIMEOFDAYKEY,
            BA.INPATIENTBILLINGLENGTHOFSTAYINDAYS,
            ROW_NUMBER() OVER (
                PARTITION BY S.SURGICALCASEKEY
                ORDER BY BA.ADMISSIONDATEKEY DESC, BA.DISCHARGEDATEKEY DESC
            ) AS RN
        FROM CTE_SURGERY_BASE S
        LEFT JOIN CABOODLE.ENCOUNTERFACT E
            ON S.HOSPITALENCOUNTERKEY = E.ENCOUNTERKEY
        LEFT JOIN CABOODLE.BILLINGACCOUNTFACT BA
            ON E.ENCOUNTERKEY = BA.PRIMARYENCOUNTERKEY
    ) X
    WHERE X.RN = 1
