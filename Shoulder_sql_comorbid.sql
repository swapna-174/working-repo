        ON SCF.PATIENTDURABLEKEY = PAT_S.DURABLEKEY
       AND PAT_S.ISCURRENT = 1
       AND PAT_S.ISVALID = 1
    INNER JOIN CABOODLE.DATEDIM DD
        ON SCF.SURGERYDATEKEY = DD.DATEKEY
     INNER JOIN CABOODLE.DEPARTMENTDIM DEP
        ON SCF.LOCATIONKEY = DEP.DEPARTMENTKEY
    WHERE DD.DATEVALUE >= DATEADD(YEAR, -5, CURRENT_DATE)
      AND EAP.PROC_CODE IN (
            '29806','23455','29828','23430','23440','23020','29825',
