CTE_CASE_PROCEDURE AS (

    WITH CPTS AS (

        SELECT
            S.SURGICALCASEKEY,
            EAP.PROC_NAME,
            EAP.PROC_CODE,
            ROW_NUMBER() OVER (
                PARTITION BY S.SURGICALCASEKEY
                ORDER BY EAP.PROC_CODE
            ) AS RN
        FROM CTE_SURGERY_BASE S
        INNER JOIN CLARITY.OR_CASE_ADDL_CODES CODES
            ON S.SURGICALCASEEPICID = CODES.CASE_ID
        INNER JOIN CLARITY.CLARITY_EAP EAP
            ON CODES.OR_CASE_ADDL_CODE_ID = EAP.PROC_ID

    )

    SELECT
        SURGICALCASEKEY,

        LISTAGG(DISTINCT PROC_NAME, ' || ')
            WITHIN GROUP (ORDER BY PROC_NAME) AS PROCEDURE_NAME,

        LISTAGG(DISTINCT PROC_CODE, ' || ')
            WITHIN GROUP (ORDER BY PROC_CODE) AS CPTCODE,

        MAX(CASE
                WHEN RN = 1 THEN PROC_CODE
            END) AS PRIMARYCPT

    FROM CPTS
    GROUP BY SURGICALCASEKEY
),
CTE_BILLING_ACCOUNT AS (
    SELECT
        X.SURGICALCASEKEY,
        X.FINANCIALCLASS,
        X.ADMISSIONDATEKEY,
