CTE_PREIMAGING AS (
    SELECT
        x.SURGICALCASEKEY,
        x.PDK,
        MAX(CASE WHEN x.modality_group = 'MRI'  AND x.rn = 1 THEN CAST(x.ExamStartInstant AS DATE) END) AS PREOP_MRI_DATE,
        MAX(CASE WHEN x.modality_group = 'XRAY' AND x.rn = 1 THEN CAST(x.ExamStartInstant AS DATE) END) AS PREOP_XRAY_DATE,
        MAX(CASE WHEN x.modality_group = 'CT'   AND x.rn = 1 THEN CAST(x.ExamStartInstant AS DATE) END) AS PREOP_CT_DATE,
        MAX(CASE WHEN x.modality_group = 'DEXA' AND x.rn = 1 THEN CAST(x.ExamStartInstant AS DATE) END) AS PREOP_DEXA_DATE,
        MAX(CASE WHEN x.modality_group = 'ULTRASOUND' AND x.rn = 1 THEN CAST(x.ExamStartInstant AS DATE) END) AS PREOP_ULTRASOUND_DATE
    FROM (
        SELECT
            S.SURGICALCASEKEY,
            S.PDK,
            img.ExamStartInstant,
            CASE
                WHEN (proceduredim.Name) iLIKE 'mr%' THEN 'MRI'
                WHEN (proceduredim.Name) iLIKE 'xr%' OR LOWER(proceduredim.Name) LIKE 'xray%' THEN 'XRAY'
                WHEN (proceduredim.Name) ILIKE 'ct%' THEN 'CT'
                WHEN (proceduredim.Name) ILIKE 'dxa%' THEN 'DEXA'
                WHEN (proceduredim.Name) ILIKE 'us%' THEN 'ULTRASOUND'
                ELSE NULL
            END AS modality_group,
            ROW_NUMBER() OVER (
                PARTITION BY S.SURGICALCASEKEY,
                             CASE
                                 WHEN (proceduredim.Name) ILIKE 'mr%' THEN 'MRI'
                                 WHEN (proceduredim.Name) ILIKE 'xr%' OR LOWER(proceduredim.Name) LIKE 'xray%' THEN 'XRAY'
                                 WHEN (proceduredim.Name) ILIKE 'ct%' THEN 'CT'
                                 WHEN (proceduredim.Name) ILIKE 'dxa%' THEN 'DEXA'
                                 WHEN (proceduredim.Name) ILIKE 'us%' THEN 'ULTRASOUND'
                                 ELSE NULL
                             END
                ORDER BY img.ExamStartInstant DESC
            ) AS rn
        FROM CTE_SURGERY_BASE S
        JOIN CABOODLE.IMAGINGFACT img
            ON S.PDK = img.PATIENTDURABLEKEY
        JOIN CABOODLE.PROCEDUREDIM proceduredim
            ON img.FIRSTPROCEDUREDURABLEKEY = proceduredim.DurableKey
        WHERE img.ExamStartInstant < CAST(S.SURGERY_DATE AS TIMESTAMP_NTZ)
          AND (
                (proceduredim.Name) ILIKE 'mr%'
             OR (proceduredim.Name) ILIKE 'xr%'
             OR (proceduredim.Name) ILIKE 'xray%'
             OR (proceduredim.Name) ILIKE 'ct%'
             OR (proceduredim.Name) ILIKE 'dxa%'
             OR (proceduredim.Name) ILIKE 'us%'
          )
    ) x
    WHERE x.modality_group IS NOT NULL
    GROUP BY
        x.SURGICALCASEKEY,
        x.PDK
