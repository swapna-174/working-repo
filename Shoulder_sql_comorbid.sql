HspReadmitDates_30 AS (
    SELECT
        SurgicalCaseKey,
        LISTAGG(TO_VARCHAR(admt_dt), ', ')
            WITHIN GROUP (ORDER BY admt_dt ASC) AS ReadmissionDate_30D,
        LISTAGG(dxname, ', ')
            WITHIN GROUP (ORDER BY admt_dt ASC) AS ReadmissionReason_30D
    FROM (
        SELECT DISTINCT
            idx.SURGICALCASEKEY AS SurgicalCaseKey,
            hdd.DATEVALUE AS admt_dt,
            COALESCE(dx.NAME, 'Not documented') AS dxname,
            ROW_NUMBER() OVER (
                PARTITION BY idx.SURGICALCASEKEY, hsp.ADMISSIONDATEKEY
                ORDER BY hsp.ADMISSIONDATEKEY ASC
            ) AS rn
        FROM CTE_INDEX_STAY idx
        JOIN CABOODLE.HOSPITALADMISSIONFACT hsp
            ON hsp.PATIENTDURABLEKEY = idx.PDK
           AND hsp.ADMISSIONDATEKEY > idx.DISCHARGEDATEKEY
        JOIN CABOODLE.DATEDIM hdd
            ON hsp.ADMISSIONDATEKEY = hdd.DATEKEY
        JOIN CABOODLE.ENCOUNTERFACT enc
            ON hsp.ENCOUNTERKEY = enc.ENCOUNTERKEY
        LEFT JOIN STAGE.RDSC.RDSC_readmission_op_35_all_table readmit
            ON enc.ENCOUNTEREPICCSN = readmit.REVISIT_ENCNTR_ID
        LEFT JOIN CABOODLE.DIAGNOSISTERMINOLOGYDIM t
            ON hsp.PRINCIPALPROBLEMKEY = t.DIAGNOSISKEY
        LEFT JOIN CABOODLE.DIAGNOSISDIM dx
            ON hsp.PRINCIPALPROBLEMKEY = dx.DIAGNOSISKEY
        WHERE t.TYPE = 'ICD-10-CM'
          AND hsp.PRINCIPALPROBLEMKEY IS NOT NULL
          AND hsp.ADMISSIONDATEKEY IS NOT NULL
          AND DATEDIFF(DAY, idx.DISCHARGE_DATE, hdd.DATEVALUE) BETWEEN 1 AND 30
        QUALIFY rn = 1
    ) s
    GROUP BY SurgicalCaseKey
